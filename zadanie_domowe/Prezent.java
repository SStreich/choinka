package zadanie_domowe;

public class Prezent {
    private String nazwa;
    private String rodzaj;

    public Prezent(String nazwa, String rodzaj) {
        this.nazwa = nazwa;
        this.rodzaj = rodzaj;
    }

    public String getNazwa() {
        return nazwa;
    }

    public String getRodzaj() {
        return rodzaj;
    }
}
